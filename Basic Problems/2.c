// Write a program to print the sum, difference, multiplication, division of two numbers.
#include <stdio.h>

int main()
{
    int a = 10, b = 20;
    printf("%d+%d=%d\n", a, b, a + b);
    printf("%d-%d=%d\n", a, b, a - b);
    printf("%d*%d=%d\n", a, b, a * b);
    printf("%d//%d=%d\n", a, b, a / b);
    return 0;
}